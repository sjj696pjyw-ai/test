def get_region_code_by_name(name: str) -> str | None:
    """Получить код региона по названию или алиасу."""
    name_lower = name.lower().strip()
    
    # Прямой поиск по названию
    for code, city_name in REGION_CITIES.items():
        if city_name.lower() == name_lower:
            return code
    
    # Поиск по алиасам
    for code, aliases in REGION_ALIASES.items():
        if name_lower in aliases:
            return code
            
    return None


def get_region_name_by_code(code: str) -> str | None:
    """Получить название региона по коду."""
    return REGION_CITIES.get(code)
